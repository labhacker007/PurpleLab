@router.get("/sources")
async def list_sources(category: str | None = Query(None)):
    """List all registered log sources with MITRE mappings and SIEM metadata."""
    from backend.log_sources.agentic_generator import get_generator
    gen = get_generator()
    sources = await gen.list_sources()
    if category:
        sources = [s for s in sources if s["category"] == category]
    return {"count": len(sources), "sources": sources}


@router.get("/sources/{source_id}")
async def get_source(source_id: str):
    """Return full schema definition for a specific source."""
    from backend.log_sources.schema_registry import get_registry
    reg = get_registry()
    schema = reg.get(source_id)
    if not schema:
        raise HTTPException(404, detail=f"Source '{source_id}' not found.")
    from dataclasses import asdict
    return asdict(schema)


@router.get("/sources/{source_id}/techniques")
async def get_source_techniques(source_id: str):
    """List MITRE techniques mapped to a source."""
    from backend.log_sources.schema_registry import get_registry
    reg = get_registry()
    schema = reg.get(source_id)
    if not schema:
        raise HTTPException(404, detail=f"Source '{source_id}' not found.")
    return {
        "source_id": source_id,
        "techniques": [
            {"technique_id": tid, "event_types": etypes}
            for tid, etypes in schema.mitre_mappings.items()
        ],
    }


