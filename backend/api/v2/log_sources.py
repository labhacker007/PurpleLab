"""Log source generation endpoints — v2 API.

Powered by the agentic log generator: schemas live in ChromaDB,
events are generated by Claude and cached in Redis/memory.
Never uses hardcoded event templates.
"""
from __future__ import annotations

import uuid
from typing import Any

from fastapi import APIRouter, HTTPException, Query
from pydantic import BaseModel, Field

router = APIRouter(prefix="/log-sources", tags=["log-sources"])


# ---------------------------------------------------------------------------
# Request/response models
# ---------------------------------------------------------------------------

class GenerateRequest(BaseModel):
    source_id: str = Field(
        ..., description="Log source ID (e.g. 'windows_sysmon', 'aws_cloudtrail')."
    )
    technique_id: str = Field(
        ..., description="MITRE ATT&CK technique ID (e.g. 'T1059.001')."
    )
    count: int = Field(10, ge=1, le=500, description="Number of events to generate.")
    snr_ratio: float = Field(
        1.0, ge=0.0, le=1.0,
        description="Signal-to-noise ratio. 1.0 = all attack. 0.1 = 10% attack + 90% noise.",
    )
    force_refresh: bool = Field(
        False, description="Bypass cache and regenerate from Claude."
    )


class ChainRunRequest(BaseModel):
    chain_id: str | None = Field(
        None, description="Built-in chain ID (use /attack-chains/list to enumerate)."
    )
    chain_yaml: str | None = Field(
        None, description="YAML definition for a custom attack chain."
    )
    simulate_delays: bool = Field(
        False, description="Apply real delays between stages (for SIEM streaming)."
    )
    parallelise: bool = Field(
        False, description="Run independent stages in parallel."
    )


class LogSourceCreateRequest(BaseModel):
    name: str = Field(..., max_length=255)
    source_type: str = Field(..., max_length=50)
    schema_definition: dict[str, Any] | None = Field(None)
    sample_log: dict[str, Any] | None = Field(None)
    enabled: bool = Field(True)
    description: str = Field("", max_length=1000)


