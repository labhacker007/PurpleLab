@router.post("/generate")
async def generate_events(req: GenerateRequest):
    """Generate synthetic attack events for a technique + source pair.

    Events are generated by Claude using the schema registry and cached
    in Redis/memory. Subsequent calls with the same parameters are fast.
    """
    from backend.log_sources.agentic_generator import get_generator
    gen = get_generator()
    result = await gen.generate(
        source_id=req.source_id,
        technique_id=req.technique_id,
        count=req.count,
        snr_ratio=req.snr_ratio,
        force_refresh=req.force_refresh,
    )
    if "error" in result:
        raise HTTPException(400, detail=result["error"])
    return result


