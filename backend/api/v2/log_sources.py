@router.post("/attack-chains/run")
async def run_attack_chain(req: ChainRunRequest):
    """Execute an attack chain and return all generated events.

    Provide either chain_id (built-in) or chain_yaml (custom YAML).
    All events are tagged with a shared correlation_id for traceability.
    """
    from backend.attack_chains.orchestrator import AttackChainOrchestrator, AttackChain
    orch = AttackChainOrchestrator()

    if req.chain_yaml:
        chain = AttackChain.from_yaml_str(req.chain_yaml)
    elif req.chain_id:
        chain = orch.get_builtin_chain(req.chain_id)
        if not chain:
            available = [c["id"] for c in orch.list_builtin_chains()]
            raise HTTPException(
                404,
                detail=f"Chain '{req.chain_id}' not found. Available: {available}",
            )
    else:
        raise HTTPException(400, detail="Provide chain_id or chain_yaml.")

    result = await orch.run(
        chain,
        simulate_delays=req.simulate_delays,
        parallelise=req.parallelise,
    )
    return result.to_dict()


