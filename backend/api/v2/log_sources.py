@router.post("/generate/technique/{technique_id}")
async def generate_for_technique(
    technique_id: str,
    count_per_source: int = Query(5, ge=1, le=100),
    snr_ratio: float = Query(1.0, ge=0.0, le=1.0),
):
    """Generate events from ALL sources that map to a MITRE technique."""
    from backend.log_sources.schema_registry import get_registry
    from backend.log_sources.agentic_generator import get_generator
    reg = get_registry()
    gen = get_generator()

    sources = reg.get_mitre_sources(technique_id)
    if not sources:
        raise HTTPException(
            404,
            detail=f"No sources mapped to technique '{technique_id}'.",
        )

    import asyncio
    tasks = [
        gen.generate(
            source_id=s.source_id,
            technique_id=technique_id,
            count=count_per_source,
            snr_ratio=snr_ratio,
        )
        for s in sources
    ]
    results = await asyncio.gather(*tasks, return_exceptions=True)

    all_events: list[dict[str, Any]] = []
    source_summaries = []
    for s, res in zip(sources, results):
        if isinstance(res, Exception):
            source_summaries.append({"source_id": s.source_id, "error": str(res)})
        else:
            all_events.extend(res.get("events", []))
            source_summaries.append({
                "source_id": s.source_id,
                "count": res.get("count", 0),
                "cache_hit": res.get("cache_hit", False),
            })

    return {
        "technique_id": technique_id,
        "total_events": len(all_events),
        "sources": source_summaries,
        "events": all_events,
    }


