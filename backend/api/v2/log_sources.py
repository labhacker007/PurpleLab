@router.get("/scoring/ihds")
async def compute_ihds(
    techniques: str = Query(
        ...,
        description="Comma-separated MITRE technique IDs in scope.",
    ),
    use_joti: bool = Query(False, description="Pull hunt score from Joti HCCS."),
):
    """Compute Intel-to-Hunt-to-Detection Score (IHDS).

    Multiplicative pipeline: Intel × Hunt × Detection.
    Any broken stage → score approaches zero.
    """
    from backend.scoring.ihds import IntelHuntDetectionScore, TTPIntelEntry, HuntEntry
    from backend.scoring.des import RuleSummary
    from datetime import datetime, timezone, timedelta

    technique_list = [t.strip() for t in techniques.split(",") if t.strip()]

    # Demo data (replace with DB queries)
    intel = [
        TTPIntelEntry("T1059.001", "APT29", datetime.now(timezone.utc) - timedelta(days=10)),
        TTPIntelEntry("T1078", "APT29", datetime.now(timezone.utc) - timedelta(days=30)),
        TTPIntelEntry("T1003.001", "Lazarus", datetime.now(timezone.utc) - timedelta(days=60)),
    ]
    hunts = [
        HuntEntry("T1059.001", "hunt-001", datetime.now(timezone.utc) - timedelta(days=7)),
        HuntEntry("T1078", "hunt-002", datetime.now(timezone.utc) - timedelta(days=20)),
    ]
    rules = [
        RuleSummary("sigma-ps-encoded", "T1059.001", 12, 10,
                    datetime.now(timezone.utc) - timedelta(days=5), 0.05),
        RuleSummary("sigma-valid-accounts", "T1078", 8, 7,
                    datetime.now(timezone.utc) - timedelta(days=15), 0.10),
    ]

    joti_hunt_score = None
    joti_used = False
    if use_joti:
        from backend.integrations.joti_client import get_joti_client
        client = get_joti_client()
        if client:
            async with client as joti:
                hccs = await joti.get_hccs()
                if hccs:
                    joti_hunt_score = hccs.score / 100.0
                    joti_used = True

    scorer = IntelHuntDetectionScore()
    result = scorer.compute(intel, hunts, rules, technique_list, joti_hunt_score)
    return result.to_dict()


